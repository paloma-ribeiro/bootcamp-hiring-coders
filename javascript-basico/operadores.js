//Operadores Aritméticos

//adição +
var sumTwoNumbers = 1 + 3;
console.log(sumTwoNumbers);

//subtração -
var subTwoNumbers = 4 - 3;
console.log(subTwoNumbers);

//multiplicação *
var multTwoNumbers = 8 * 3;
console.log(multTwoNumbers);

//divisão /
var divTwoNumbers = 8 / 3;
console.log(divTwoNumbers);

//módulo ou resto de uma divisão %
console.log(8 % 3);

//incremento ++
var num = 1;
console.log(++num); // ++num é a mesma coisa que num = num + 1;

//decremento --
var num = 2;
console.log(--num); // --num é a mesma coisa que num = num - 1;

//Operadores de Atribuição
// = atribuição

//atribuição de soma

var x = 1;
var y = 3;
console.log((x += y)); // essa expressão é a mesma coisa que x = x + y;

//atribuição de subtração
console.log((x -= y));

//atribuição de multiplicação
console.log((x *= y));

//atribuição de divisão
console.log((x /= y));

